# MAGIC # Unity Catalog
# MAGIC
# MAGIC Unity Catalog es un sistema de gobernanza de datos en Databricks que permite gestionar, auditar y asegurar el acceso a datos en la plataforma.
# MAGIC
# MAGIC ## Beneficios
# MAGIC
# MAGIC * Unifica la gestión de datos en todos los workspaces de Databricks.
# MAGIC * Permite control de acceso centralizado a nivel de tabla, columna y fila.
# MAGIC * Facilita el descubrimiento de datos mediante catálogos, esquemas y tablas.
# MAGIC * Proporciona auditoría y trazabilidad de accesos y cambios.
# MAGIC
# MAGIC ## Problema que resuelve
# MAGIC
# MAGIC Elimina la complejidad de gestionar permisos y gobernanza de datos dispersos en múltiples workspaces y sistemas, centralizando la administración y mejorando la seguridad y cumplimiento.
